// program to check how sizeof operator work
#include<stdio.h>
void main(){
    int i;
    char c;
    float f;
    double d;

    printf("Size of Integer : %d\n",sizeof(i));
    printf("Size of Character : %d\n",sizeof(c));
    printf("Size of Float : %d\n",sizeof(f));
    printf("Size of Double : %d\n",sizeof(d));

}